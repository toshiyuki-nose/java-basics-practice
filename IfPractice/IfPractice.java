public class IfPractice {
    public static void main(String[] args) {
        int score = 75;

        if (score >= 80) {
            System.out.println("合格！");
        } else if (score >= 60) {
            System.out.println("あと少し！");
        } else {
            System.out.println("不合格…");
        }
    }
}
